
export var fun = function (no1:number,no2:number)
{
    return no1+no2;
}


export function addition(no1:number , no2:number):number
{
    var ans : number=0;
    ans=no1+no2;
    return ans;
}

export function subtraction(no1:number , no2:number):number
{
    var ans : number=0;
    ans=no1-no2;
    return ans;
}



